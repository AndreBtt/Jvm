
public class Engenheiro extends Empregado{
	int nroHorasTransito;
	public Engenheiro(String name,String lastName,float salary,
		int numberOfHours,int identification){
		super(name,lastName,salary,numberOfHours,identification);
		nroHorasTransito = 0;
	}
	public void horasTrafegadas(int horas){
		nroHorasTransito = horas;
		nroHoras+=horas;
	}

}
